package com.fsd.salary.model;

public class Salary {

	private Double salary;
	private Double inquota;
	private Double dequota;
	private Integer count;
	private Double amount;
	private Integer year;
	public Double getSalary() {
		return salary;
	}
	public void setSalary(Double salary) {
		this.salary = salary;
	}
	public Double getInquota() {
		return inquota;
	}
	public void setInquota(Double inquota) {
		this.inquota = inquota;
	}
	public Double getDequota() {
		return dequota;
	}
	public void setDequota(Double dequota) {
		this.dequota = dequota;
	}
	public Integer getCount() {
		return count;
	}
	public void setCount(Integer count) {
		this.count = count;
	}
	public Double getAmount() {
		return amount;
	}
	public void setAmount(Double amount) {
		this.amount = amount;
	}
	public Integer getYear() {
		return year;
	}
	public void setYear(Integer year) {
		this.year = year;
	}

	

	
	
	
	

}
